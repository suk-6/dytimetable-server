const mongoose = require('mongoose');
const { Schema } = mongoose;

const noticeSchema = new Schema({
    sender: {
        type: String,
        required: true,
    },
    title: {
        type: String,
        required: true,
    },
    content: {
        type: String,
        required: true,
    },
    receiver: {
        type: String,
        required: true,
    },
    createdAt: {
        type: Date,
        default: Date.now,
    },
});

module.exports = mongoose.model('Notice', noticeSchema);